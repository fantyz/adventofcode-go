type Cart struct {
	x, y     int
	vx, vy   int
	nextTurn Dir
}

type Dir int

const (
	LeftDir Dir = iota
	StraightDir
	RightDir
)

type Rail int

const (
	None Rail = iota
	Vert
	Hori
	Right
	Left
	Inter
)

func (r Rail) String() string {
	switch r {
	case None:
		return " "
	case Vert:
		return "|"
	case Hori:
		return "-"
	case Right:
		return "/"
	case Left:
		return "\\"
	case Inter:
		return "+"
	default:
		panic("unknown rail")
	}
}

type Tracks struct {
	tracks [][]Rail
	carts  []*Cart
}

func NewTracks(in string) *Tracks {
	t := Tracks{}

	for _, l := range strings.Split(in, "\n") {
		if len(l) == 0 {
			continue
		}
		row := make([]Rail, len(l))
		for i := range l {
			var rail Rail
			switch l[i] {
			case '|':
				rail = Vert
			case '-':
				rail = Hori
			case '/':
				rail = Right
			case '\\':
				rail = Left
			case '+':
				rail = Inter
			case '^':
				rail = Vert
				t.carts = append(t.carts, &Cart{i, len(t.tracks), 0, -1, LeftDir})
			case '>':
				rail = Hori
				t.carts = append(t.carts, &Cart{i, len(t.tracks), 1, 0, LeftDir})
			case 'v':
				rail = Vert
				t.carts = append(t.carts, &Cart{i, len(t.tracks), 0, 1, LeftDir})
			case '<':
				rail = Hori
				t.carts = append(t.carts, &Cart{i, len(t.tracks), -1, 0, LeftDir})
			default:
				rail = None
			}
			row[i] = rail
		}
		t.tracks = append(t.tracks, row)
	}

	return &t
}

func (t *Tracks) RunUntilCrash() (int, int, int) {
	for i := 1; ; i++ {
		if x, y, crash := t.Tick(true); crash {
			return x, y, i
		}
	}
}

func (t *Tracks) RunUntilOneCartLeft() (int, int, int) {
	for i := 1; ; i++ {
		if x, y, crash := t.Tick(false); crash {
			if len(t.carts) == 1 {
				return x, y, i
			}
		}
	}
}

func sortCarts(carts []*Cart) {
	sort.Slice(carts, func(i, j int) bool {
		return carts[i].y < carts[j].y || (carts[i].y == carts[j].y && carts[i].x < carts[j].x)
	})
}

func (t *Tracks) Tick(stopOnCrash bool) (int, int, bool) {
	sortCarts(t.carts)
	deadCarts := map[int]struct{}{}

	for i, c := range t.carts {
		if _, dead := deadCarts[i]; dead {
			continue
		}

		c.x, c.y = c.x+c.vx, c.y+c.vy
		for j, oc := range t.carts {
			if _, dead := deadCarts[i]; dead {
				continue
			}

			if i != j && c.x == oc.x && c.y == oc.y {
				if stopOnCrash {
					return c.x, c.y, true
				}
				deadCarts[i] = struct{}{}
				deadCarts[j] = struct{}{}
			}
		}
		//fmt.Printf("Cart %p moved to (%d, %d) with direction (%d, %d)\n", c, c.x, c.y, c.vx, c.vy)

		switch t.tracks[c.y][c.x] {
		case Right:
			c.vx, c.vy = -c.vy, -c.vx
		case Left:
			c.vx, c.vy = c.vy, c.vx
		case Inter:
			switch c.nextTurn {
			case LeftDir:
				c.vx, c.vy = c.vy, -c.vx
				c.nextTurn = StraightDir
			case StraightDir:
				c.nextTurn = RightDir
			case RightDir:
				c.vx, c.vy = -c.vy, c.vx
				c.nextTurn = LeftDir
			}
		}
	}

	if len(deadCarts) > 0 {
		living := make([]*Cart, 0, len(t.carts)-len(deadCarts))
		for i := 0; i < len(t.carts); i++ {
			if _, dead := deadCarts[i]; !dead {
				living = append(living, t.carts[i])
			}
		}
		t.carts = living
	}

	/*
		fmt.Println(t.String(true))
		fmt.Println()
		for _, c := range t.carts {
			fmt.Println(c)
			break
		}
		fmt.Println()
	*/

	x, y := -1, -1
	if len(t.carts) > 0 {
		x, y = t.carts[0].x, t.carts[0].y
	}

	return x, y, len(deadCarts) > 0
}

func (t *Tracks) String(debug bool) string {
	sortCarts(t.carts)

	var str string
	idx := 0
	for y := 0; y < len(t.tracks); y++ {
		str += "\n"
		for x := 0; x < len(t.tracks[y]); x++ {
			if idx < len(t.carts) && t.carts[idx].x == x && t.carts[idx].y == y {
				if debug {
					str += "\033[1;31m"
				}
				switch {
				case t.carts[idx].vx == 1:
					str += ">"
				case t.carts[idx].vx == -1:
					str += "<"
				case t.carts[idx].vy == 1:
					str += "v"
				case t.carts[idx].vy == -1:
					str += "^"
				default:
					panic("wat..?")
				}
				if debug {
					str += "\033[0m"
				}

				idx++
			} else {
				str += t.tracks[y][x].String()
			}
		}
		if debug && y > 40 {
			break
		}
	}

	return str
}

