import (
	"fmt"
	"strings"
)

func main() {
	l := &Lights{}
	for y, line := range strings.Split(puzzleInput, "\n") {
		for x := 0; x < len(line); x++ {
			if line[x] == '#' {
				l.On(x, y)
			}
		}
	}

	for i := 0; i < 100; i++ {
		l.Animate()
	}

	fmt.Println(l.Lights())
}

type Lights struct {
	f [100][100]bool
}

func (l *Lights) On(x, y int) {
	l.f[y][x] = true
}

func (l *Lights) Off(x, y int) {
	l.f[y][x] = false
}

func (l *Lights) Lights() int {
	sum := 0
	for y := 0; y < len(l.f); y++ {
		for x := 0; x < len(l.f[y]); x++ {
			if l.f[y][x] {
				sum++
			}
		}
	}
	return sum
}

func (l *Lights) Animate() {
	oldF := l.f
	for y := 0; y < len(l.f); y++ {
		for x := 0; x < len(l.f[y]); x++ {
			if (y == 0 && x == 0) || (y == 0 && x == len(l.f[0])-1) || (y == len(l.f)-1 && x == len(l.f[0])-1) || (y == len(l.f)-1 && x == 0) {
				l.f[y][x] = true
				continue
			}

			neighbours := 0
			if x-1 >= 0 && y+1 < len(oldF) && oldF[y+1][x-1] {
				neighbours++
			}
			if x-1 >= 0 && oldF[y][x-1] {
				neighbours++
			}
			if x-1 >= 0 && y-1 >= 0 && oldF[y-1][x-1] {
				neighbours++
			}
			if x+1 < len(oldF[0]) && y+1 < len(oldF) && oldF[y+1][x+1] {
				neighbours++
			}
			if x+1 < len(oldF[0]) && oldF[y][x+1] {
				neighbours++
			}
			if x+1 < len(oldF[0]) && y-1 >= 0 && oldF[y-1][x+1] {
				neighbours++
			}
			if y+1 < len(oldF) && oldF[y+1][x] {
				neighbours++
			}
			if y-1 >= 0 && oldF[y-1][x] {
				neighbours++
			}

			if oldF[y][x] && neighbours != 2 && neighbours != 3 {
				l.f[y][x] = false
			}
			if !oldF[y][x] && neighbours == 3 {
				l.f[y][x] = true
			}
		}
	}
}

